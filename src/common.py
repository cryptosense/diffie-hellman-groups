DhParams = namedtuple('DhParams', 'p g')


def bytes_from_c_array(string: str, order: str = 'big') -> str:
    if order not in ('big', 'little'):
        raise ValueError('order must be either \'little\' or \'big\'')

    hexs = (h[2:] for h in re.sub(r'[\sUL{}]+', '', string).rstrip(',').split(','))
    if order == 'little':
        hexs = reversed(tuple(hexs))
    return binascii.unhexlify(''.join(hexs))


def int_from_hex(string):
    string = re.sub(r'[\s]+', '', string)
    return int(string, 16)


def int_from_c_array(string):
    binary = bytes_from_c_array(string)
    return int.from_bytes(binary, byteorder='big')


def int_from_bignum(string):
    binary = bytes_from_c_array(string, order='little')
    return int.from_bytes(binary, byteorder='big')


def int_from_radix64(string):
    character_map = '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz+/'
    return sum(
        character_map.index(char) * (64 ** index)
        for (index, char) in enumerate(reversed(string))
    )


def params_from_ber(binary):
    asn1 = pyasn1.codec.ber.decoder.decode(binary)
    return DhParams(p=int(asn1[0][0]), g=int(asn1[0][1]))


def params_from_c_array_ber(string):
    binary = bytes_from_c_array(string)
    return params_from_ber(binary)


def params_from_pem_body(string):
    binary = base64.b64decode(string)
    return params_from_ber(binary)


class Group(namedtuple('GroupBase', 'name g p length')):
    def __new__(cls, *args, **kwargs):
        self = super().__new__(cls, *args, **kwargs)
        assert self.p >> (self.length - 1) == 1
        return self

    def to_dict(self):
        return {
            'name': self.name,
            'g': self.g,
            'p': self.p,
            'length': self.length,
        }

    @classmethod
    def from_pem_body(cls, name, pem_body, length):
        binary = base64.b64decode(pem_body)
        params = params_from_ber(binary)
        return cls(name=name, g=params.g, p=params.p, length=length)


