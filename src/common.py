import re
from collections import namedtuple

import pyasn1.codec.ber.decoder


class Group(namedtuple('Group', 'p length')):
    def __new__(cls, *args, **kwargs):
        self = super().__new__(cls, *args, **kwargs)
        assert self.p >> (self.length - 1) == 1
        return self

    def to_dict(self):
        return {'p': self.p, 'length': self.length}


def from_hex(string, length):
    string = re.sub(r'[\s]+', '', string)
    return Group(p=int(string, 16), length=length)


def bytes_from_c_array(string, order: str = 'big'):
    if order not in ('big', 'little'):
        raise ValueError('order must be either \'little\' or \'big\'')

    hexs = (h[2:] for h in re.sub(r'[\sUL{}]+', '', string).rstrip(',').split(','))
    if order == 'little':
        hexs = reversed(tuple(hexs))
    return binascii.unhexlify(''.join(hexs))


def from_c_array(string, length):
    binary = bytes_from_c_array(string)
    return Group(p=int.from_bytes(binary, byteorder='big'), length=length)


def from_bignum(string, length):
    binary = bytes_from_c_array(string, order='little')
    return Group(p=int.from_bytes(binary, byteorder='big'), length=length)


def from_ber(binary, length):
    asn1 = pyasn1.codec.ber.decoder.decode(binary)
    return Group(p=int(asn1[0][0]), length=length)


def from_c_array_ber(string, length):
    binary = bytes_from_c_array(string)
    return from_ber(binary, length)


def from_pem_body(string, length):
    binary = base64.b64decode(string)
    return from_ber(binary, length)


